-- EMPLOYEE01 테이블 생성 (테이블)
CREATE TABLE EMPLOYEE01
( EMPLOYEE_ID     NUMBER(6)
, FIRST_NAME      VARCHAR2(20)
, LAST_NAME       VARCHAR2(25)
, EMAIL           VARCHAR2(25)
, PHONE_NUMBER    VARCHAR2(20)
, HIRE_DATE       DATE 
, JOB_ID          VARCHAR2(10)
, SALARY          NUMBER(8,2)
, COMMISSION_PCT  NUMBER(2,2)
, MANAGER_ID      NUMBER(6)
, DEPARTMENT_ID   NUMBER(4)

, CONSTRAINT EMP01_EMP_ID_PK PRIMARY KEY(EMPLOYEE_ID)
, CONSTRAINT EMP01_DEPT_FK FOREIGN KEY(DEPARTMENT_ID)
             REFERENCES DEPARTMENTS01(DEPARTMENT_ID)
, CONSTRAINT EMP01_JOB_FK FOREIGN KEY(JOB_ID)
             REFERENCES JOBS01(JOB_ID)
, CONSTRAINT EMP01_MANAGER_FK	FOREIGN KEY(MANAGER_ID)
             REFERENCES EMPLOYEES01(MANAGER_ID)
             
, CONSTRAINT EMP01_LAST_NAME_NN CHECK(LAST_NAME IS NOT NULL)
, CONSTRAINT EMP01_EMAIL_NN CHECK(EMAILE IS NOT NULL)
, CONSTRAINT EMP01_HIRE_DATE_NN CHECK(HIRE_DATE IS NOT NULL)
, CONSTRAINT EMP01_JOB_NN CHECK(JOB_ID IS NOT NULL)

, CONSTRAINT EMP01_SALARY_MIN CHECK(SALARY > 0)
, CONSTRAINT EMP01_EMAIL_UK UNIQUE(EMAIL)

);


